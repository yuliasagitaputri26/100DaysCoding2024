package Day10;

public class Hari17 {

    public static void main(String[] args) {

        //Operator penugasan : pengisisan dan perkalian , pengisian dan pembagian
        //Deklarasi dan Inisialisasi variabel nilai1 dan nilai2
        int nilai1 = 20;
        int nilai2 = 40;
        //mencetak nilai dari varibel nilai1 dan nilai2 
        System.out.println(nilai1);
        System.out.println(nilai2);

        //operator penugasan dengan cara mengisi dan mengalikan
        nilai1 *= 2;
        System.out.println("Nilai setelah dikalikan : " + nilai1);

        //Mengisi dan membagi
        nilai2 /= 4;
        System.out.println("Nilai setelah dibagi : " + nilai2);

    }

}
