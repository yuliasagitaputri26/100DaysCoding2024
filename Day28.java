package Day10;

public class Hari28 {
   
    public static void main(String[] args) {
        
        //konversi String ke byte
        String nilai = "100";
        byte nilai1 = Byte.parseByte(nilai);
        System.out.println("Nilai yang telah di konversi adalah : " + nilai1);
        //konversi String ke short
        String nilai2 = "101";
        short nilai3 = Short.parseShort(nilai2);
        System.out.println("Nilai yang telah di konversi adalah : " + nilai3);
    }

}
