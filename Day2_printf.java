package day2;

public class Day2 {

    public static void main(String[] args) {
        // Deklarasi Variabel
        String Asal = "Papalang";
        int Umur = 19;
        String Nama = "Yulia sagita putri";

        // Mencetak Menggunakan Printf
        System.out.printf(" Nama: %s \n ", Nama);
        System.out.printf("Asal: %s \n ", Asal);
        System.out.printf("Umur: %d Tahun \n ", Umur);

        // Menggabungkan format dalam satu baris
        System.out.printf("Hai, Nama saya %s, saya berusia %d Tahun dan saya berasal dari %s \n ", Nama, Umur, Asal);

    }

}
