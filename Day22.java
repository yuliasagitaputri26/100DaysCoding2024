package Day10;

public class Hari22 {

    public static void main(String[] args) {

        //operator penugasan : pengisian nilai,pengisian dan penambahan,pengisian dan pengurangan
        int nilai = 10;//pengisian nilai 
        nilai += 30;//pengisian dan penambahan
        System.out.println("Hasil penambahanya adalah : " + nilai);
        nilai -= 20;//pengisian dan pengurangan
        System.out.println("hasil pengurangan : " + nilai);

    }

}
