package Day10;

import java.util.Scanner;

public class Hari53 {

    public static void main(String[] args) {
        //looping: for

        Scanner yulia = new Scanner(System.in);
        System.out.print("Masukkan angka: ");
        int angka = yulia.nextInt();

        //
        for (int i = 1; i <= angka; i++) {
            System.out.println(i);

        }
    }

}
