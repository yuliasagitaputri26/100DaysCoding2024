package Day10;

public class Hari37 {

    public static void main(String[] args) {
        //operator penugasan: pengisian nilai, pengisian dan penjumlahan,pengisian dan pengurangan

        int a = 20; //pengisian nilai
        System.out.println(a);

        a += 10;//pengisian dan penambahan
        System.out.println("Nilai setelah penambahan: " + a);

        a -= 5;//pengisian dan pengurangan
        System.out.println("Nilai setelah pengurangan: " + a);
    }

}
