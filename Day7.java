package biodata;

public class Variabel_Inisialisasi {

    public static void main(String[] args) {
        //Deklarasi dan inisialisasi String
        String nama;
        nama = "kina";
        
        int jumlah_saudara;//Deklarasi variabel
        jumlah_saudara = 4;//Inisialisasi variabel di baris berikutnya
        
        boolean dataDiatas;
        dataDiatas = false;//Inisialisasi terpisah setelah deklarasi

        System.out.println("Nama saya : " + nama);
        System.out.println("Saya : " + jumlah_saudara + " Bersaudara");
        System.out.println("Ya data diatas : " + dataDiatas);

    }

}
