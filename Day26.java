package latihan;

public class Mahasiswa {
 
    public static void main(String[] args) {

        //Konversi tipe data int ke String
        int a = 80;
        String b = String.valueOf(a);
        System.out.println("Nilai yang dikonversi : " + b);
        
        //konversi tipe data float ke String
        float c = 80.0f;
        String d = String.valueOf(c);
        System.out.println("Nilai yang dikonversi : " + d);

    }

}
