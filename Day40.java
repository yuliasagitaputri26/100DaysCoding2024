package Day10;

public class Hari40 {

    public static void main(String[] args) {
        //Operator perbandingan: Lebih besar dan lebih kecil
        int nilaiPertama = 120;
        int nilaiKedua = 200;

        int hasil = nilaiPertama - nilaiKedua;
        int hasil1 = nilaiKedua * nilaiPertama;
        System.out.println(hasil);
        System.out.println(hasil1);

        System.out.println(hasil < hasil1);
        System.out.println(hasil1 < hasil);
        System.out.println(hasil1> hasil);
        System.out.println(hasil > hasil1);

    }

}
