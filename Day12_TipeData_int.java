package Day10;

public class Hari12 {

    public static void main(String[] args) {
        //Inisialisasi
        int a = 4;
        int b = 6;
        int c = 10;
        int d = 20;
        //Mencetak nilai menggunakan printf
        System.out.printf("Nilai dari A adalah %d \n", a);
        System.out.printf("Nilai dari B adalah %d \n", b);
        System.out.printf("Nilai dari C adalah %d \n", c);
        System.out.printf("Nilai dari D adalah %d \n", d);

        //Update nilai
        a = 100;
        b = 40;
        c = 60;

        //Mencetak nilai yang telah diUpdate
        System.out.println("Nilai A setelah di update = " + a);
        System.out.println("Nilai B setelah di update = " + b);
        System.out.println("Nilai C setelah di update = " + c);

    }

}
