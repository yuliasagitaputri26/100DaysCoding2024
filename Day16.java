package Day10;

public class Hari16 {

    public static void main(String[] args) {

        //Operasi Aritmatika (penjumlahan,pengurangan,Perkalian, pembagian dan modulus)
        
        //variabel a dan b diinisialisasi dengan nilai 10 dan 2
        int a = 10;
        int b = 2;
        
        //Melakukan beberapa operasi
        int c = a + b;
        int d = a - b;
        int e = a * b;
        int f = a / b;
        int g = a % b;
        //Mencetak untuk menampilkan hasil dari operasi di atas
        System.out.println("Hasil penjumlahan = " + c);
        System.out.println("Hasil pengurangan = " + d);
        System.out.println("Hasil perkalian = " + e);
        System.out.println("Hasil pembagian = " + f);
        System.out.println("Hasil sisa bagi = " + g);
    }

}
