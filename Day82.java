package Day10;

public class Hari82 {

    public static void main(String[] args) {
        //Method ceil,floor,dan round
 
        double nilai = 6.3;

        System.out.println("Nilai awal: " + nilai);

        System.out.println("Nilai ceil (dibulatkan ke atas): " + Math.ceil(nilai));//math.ceil untuk membulatkan keatas
        System.out.println("Nilai floor (dibulatkan ke bawah): " + Math.floor(nilai));//math.floor untuk membulatkan kebawah
        System.out.println("Nilai round (dibulatkan ke angka terdekat): " + Math.round(nilai));//math.roound untuk membulatkan angka yang terdekat

    }

}
